
public class TP2 {

	public static void main(String[] args) {

		int grandEntier = 123456789;
		float grandDecimal = 123456789f;
		
		System.out.println(grandEntier);
		System.out.println(grandDecimal);
		
		
		float x = 15f;
		float y = -123f;
		float z = 0f;
		float a = x/z;
		float b = y/z;
		float c = a/b;
	
		
		System.out.println("x = "+x);
		System.out.println("y = "+y);
		System.out.println("z = "+z);
		System.out.println("a = "+a);
		System.out.println("b = "+b);
		System.out.println("c = "+c);

	}
}
